    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        try {
            FileInputStream in = new FileInputStream(getClass().getResource("/org/pii/invbienes/settings.ini").getFile());
            Properties prop = new Properties();
            prop.load(in);
            prop.put("IP", ip_txt.getText());
            prop.put("DATABASE", db_txt.getText());
            prop.put("USER", us_txt.getText());
            prop.put("PWD", pwd_txt.getText());
            in.close();
            
            FileOutputStream out = new FileOutputStream(getClass().getResource("/org/pii/invbienes/settings.ini").getFile());
            prop.store(out, null);
            out.close();
            
            JOptionPane.showMessageDialog(null, "¡Éxito!", ".:: Sistema de Inventario de Bienes del Programa de Informática Integral ::.", JOptionPane.INFORMATION_MESSAGE);
            settingsPage.setVisible(false);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "ERROR: " + e.getLocalizedMessage(), ".:: Sistema de Inventario de Bienes del Programa de Informática Integral ::.", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnOKActionPerformed

