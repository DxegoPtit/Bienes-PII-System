    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String usr, pwd, repwd, name;

        usr = txt_usereg.getText();
        name = txt_name.getText();

        get_reg_pwd = txt_pwdreg.getPassword();
        pwd = String.valueOf(get_reg_pwd);
        get_reg_pwd2 = txt_pwdreg2.getPassword();
        repwd = String.valueOf(get_reg_pwd2);

        SqlControllerClass scc = new SqlControllerClass();
        if (usr.isEmpty() || pwd.isEmpty() || repwd.isEmpty() || name.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Hay información incorrecta en lo suministrado: \nAlguno de los campos solicitados está vacío.", ".:: Sistema de Inventario de Bienes del Programa de Informática Integral ::.", JOptionPane.ERROR_MESSAGE);
        } else {
            //procede a revisar si las contraseñas son iguales
            if (pwd.equals(repwd)) {
                //revisa si es de 8 caracteres o mas
                if (pwd.length() >= 8) {
                    //registra
                    if (scc.setNewAccount(name, usr, pwd)) {
                        JOptionPane.showMessageDialog(null, "¡Registrado exitosamente!", ".:: Sistema de Inventario de Bienes del Programa de Informática Integral ::.", JOptionPane.INFORMATION_MESSAGE);
                        RegistroFrame.setVisible(false);
                    } else {
                        JOptionPane.showMessageDialog(null, "Ha ocurrido un error: \nPuede que no haya conexión a la Base de Datos, o no sre haya logrado registrar  exitosamente.", ".:: Sistema de Inventario de Bienes del Programa de Informática Integral ::.", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Su contraseña es de menos de 8 caracteres.", ".:: Sistema de Inventario de Bienes del Programa de Informática Integral ::.", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Hay información incorrecta en lo suministrado: \nLas contraseñas no coinciden.", ".:: Sistema de Inventario de Bienes del Programa de Informática Integral ::.", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

